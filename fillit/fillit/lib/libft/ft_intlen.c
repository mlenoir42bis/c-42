int	ft_intlen(int n, int len)
{
	if (n < 0)
	{
		n = n * -1;
		len++;
	}
	if (n <= 9)
	{
		len++;
		return (len);
	}
	else
	{
		len++;
		return (ft_intlen(n / 10, len));
	}
}
