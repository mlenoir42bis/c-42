#include <libft.h>

char	**ft_freetab(char **tab, int i)
{
	if (tab && i > 0)
	{
		while (i >= 0)
		{
			free(tab[i]);
			i--;
		}
		free(tab);
		tab = NULL;
	}
	return (tab);
}
