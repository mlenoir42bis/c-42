#include "../includes/header.h"

int		ft_atoi(char *nbr)
{
	int res;
	int i;

	res = 0;
	i = 0;
	if (nbr[0] == '-' || nbr[0] == '+')
		i++;
	while (nbr[i] <= '9' && nbr[i] >= '0')
	{
		res = nbr[i] - '0' + res * 10;
		i++;
	}
	return (res);
}
